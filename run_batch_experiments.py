"""Run all algorithms on many QAP instances and save metrics.

This script expects instance files generated by ``generate_instances.py``
in an ``instances/`` directory, named as "{size}-{instance}.npz".
Each file must contain arrays "F" and "D".

For each size in [10, 15, 20, 25, 50, 75, 100] and for each
instance index in 1..100, this script:

- loads F, D from the corresponding .npz file
- builds a simple permutation seed of length N
- calls ``calculation.run_all_calculations_bundle``
- writes per-algorithm metrics to a CSV file ``batch_results.csv``

Usage:

    python run_batch_experiments.py
"""

from __future__ import annotations

from pathlib import Path
import csv

import numpy as np

from calculation import run_all_calculations_bundle


SIZES = [10, 15, 20, 25, 50, 75, 100]
INSTANCES_PER_SIZE = 100
INSTANCES_DIR = Path("instances")
OUTPUT_CSV = Path("batch_results.csv")


def main() -> None:
    if not INSTANCES_DIR.is_dir():
        raise SystemExit(
            f"Instances directory '{INSTANCES_DIR}' not found. "
            "Run generate_instances.py first."
        )

    fieldnames = [
        "size",
        "instance",
        "algorithm",
        "time_taken",
        "steps",
        "computational_cost",
        "best_energy",
        "time_to_best",
        "min_energy",
        "median_energy",
        "success_probability",
        "standard_deviation",
        "residual_energy",
    ]

    with OUTPUT_CSV.open("w", newline="") as f_out:
        writer = csv.DictWriter(f_out, fieldnames=fieldnames)
        writer.writeheader()

        for N in SIZES:
            for inst_idx in range(1, INSTANCES_PER_SIZE + 1):
                fname = INSTANCES_DIR / f"{N}-{inst_idx}.npz"
                if not fname.is_file():
                    raise SystemExit(f"Missing instance file: {fname}. Generate it first.")

                data = np.load(fname)
                if "F" not in data or "D" not in data:
                    raise SystemExit(f"File {fname} must contain 'F' and 'D' arrays.")

                F = np.asarray(data["F"], dtype=float)
                D = np.asarray(data["D"], dtype=float)

                if F.shape[0] != F.shape[1] or D.shape[0] != D.shape[1] or F.shape != D.shape:
                    raise SystemExit(f"File {fname} has inconsistent shapes: F {F.shape}, D {D.shape}.")

                n = F.shape[0]
                # Simple deterministic seed: permutation 0..n-1
                permutation_seed = np.arange(n, dtype=float)

                bundle = run_all_calculations_bundle(permutation_seed, F, D)
                metrics = bundle["metrics"]  # dict[algo_name -> metric dict]

                for algo_name, m in metrics.items():
                    row = {
                        "size": N,
                        "instance": inst_idx,
                        "algorithm": algo_name,
                    }
                    for key in fieldnames[3:]:  # metric keys
                        row[key] = float(m[key])
                    writer.writerow(row)

                # Optional: print progress; comment if too verbose.
                # print(f"Processed size={N}, instance={inst_idx}")


if __name__ == "__main__":
    main()
